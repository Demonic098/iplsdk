/* Definitions of physical drive number for each drive */
#define DEV_MS0		0

/*-----------------------------------------------------------------------*/
/* Get Drive Status                                                      */
/*-----------------------------------------------------------------------*/

DSTATUS disk_status (
	BYTE pdrv		/* Physical drive nmuber to identify the drive */
)
{
	DSTATUS status = STA_NOINIT;

	if (pdrv == DEV_MS0) {
		status = 0;
	}
	
	return status;
}



